##Cálculo de área e perímetro de um triângulo.
##Habilidade Cobrada: Aplicação de fórmulas matemáticas em programação.

base = float(input("Digite a medida da base do triângulo: "))
altura = float(input("Digite a medida de altura do triângulo: "))
area = (base * altura) / 2
print(f"A área do triangulo é {area} e o perimetro é indeterminado sem os lados restantes. ")