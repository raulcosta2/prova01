##Habilidade Cobrada: Aplicação de lógica condicional para tomada de decisões.
numero = float(input("Digite um numero: "))
if numero > 0:
    print("O número é positivo.")
elif numero < 0:
    print("O numero é negativo.")
else:
    print("O número é zero.")